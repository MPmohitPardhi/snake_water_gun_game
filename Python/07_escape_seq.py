# Newline
print("Hello\nWorld")  # Output:
# Hello
# World

# Tab
print("Hello\tWorld")  # Output: Hello    World

# Backslash
print("This is a backslash: \\")  # Output: This is a backslash: \

# Single Quote
print('It\'s Python')  # Output: It's Python

# Double Quote
print("He said, \"Python is awesome!\"")  # Output: He said, "Python is awesome!"

# Backspace
print("Hello\bWorld")  # Output: HellWorld (backspaces one character)

# Carriage Return
print("12345\rAB")  # Output: AB345 (overwrites the beginning)

# Form Feed (rarely used)
print("Hello\fWorld")  # Output: HelloWorld

# Unicode Character (U+00A9 is ©)
print("\u00A9 2025")  # Output: © 2025

# Hex Value
print("\x48\x69")  # Output: Hi (ASCII of 0x48 = 'H', 0x69 = 'i')
